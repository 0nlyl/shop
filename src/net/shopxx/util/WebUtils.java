	/**
	 * POST请求
	 *
	 * @param url
	 *            URL
	 * @param params
	 *            请求参数
	 * @return 返回结果
	 */
	public static String post(String url, org.apache.commons.httpclient.NameValuePair[] params) throws BusinessException {
		Assert.hasText(url);

		String result = null;
		List<org.apache.http.NameValuePair> nameValuePairs = new ArrayList<>();
		if (params != null) {
			for (org.apache.commons.httpclient.NameValuePair entry : params) {
				String name = entry.getName();
				String value = ConvertUtils.convert(entry.getValue());
				if (StringUtils.isNotEmpty(name)) {
					nameValuePairs.add(new BasicNameValuePair(name, value));
				}
			}
		}
		HttpPost httpPost = new HttpPost(url);
		try {
			httpPost.setEntity(new UrlEncodedFormEntity(nameValuePairs, "UTF-8"));
		} catch (UnsupportedEncodingException e) {
			throw new BusinessException(ErrorCode.WEB_ERROR_URLENCODE);
		}
		CloseableHttpResponse httpResponse = null;
		try {
			httpResponse = HTTP_CLIENT.execute(httpPost);
		} catch (IOException e) {
			throw new BusinessException(ErrorCode.WEB_ERROR_HTTPREQUESTFAIL);
		}
		try {
			HttpEntity httpEntity = httpResponse.getEntity();
			if (httpEntity != null) {
				result = EntityUtils.toString(httpEntity);
				EntityUtils.consume(httpEntity);
			}
		} catch (IOException e) {
			throw new BusinessException(ErrorCode.WEB_ERROR_HTTPRESPONSETOSTRING);
		} finally {
			try {
				httpResponse.close();
			} catch (IOException e) {
				e.printStackTrace();
			}
		}
		return result;
	}


	/**
	 * 需要证书的请求
	 * @param url String
	 * @param parameterMap post的请求数据  Map
	 * @param inputStream 证书输入流
	 * @param password	证书密码
	 * @param connectTimeoutMs 超时时间，单位是毫秒
	 * @param readTimeoutMs 超时时间，单位是毫秒
	 * @return API返回数据
	 * @throws Exception
	 */
	public static String postWithCert(String url, Map<String, String> parameterMap,InputStream inputStream,String password,int connectTimeoutMs, int readTimeoutMs) throws Exception {
		String UTF8 = "UTF-8";
		String reqBody = WXPayUtil.mapToXml(parameterMap);
		URL httpUrl = new URL(url);
		char[] pwd = password.toCharArray();
		InputStream certStream = inputStream;
		KeyStore ks = KeyStore.getInstance("PKCS12");
		ks.load(certStream, pwd);

		// 实例化密钥库 & 初始化密钥工厂
		KeyManagerFactory kmf = KeyManagerFactory.getInstance(KeyManagerFactory.getDefaultAlgorithm());
		kmf.init(ks, pwd);

		// 创建SSLContext
		SSLContext sslContext = SSLContext.getInstance("TLS");
		sslContext.init(kmf.getKeyManagers(), null, new SecureRandom());
		HttpsURLConnection.setDefaultSSLSocketFactory(sslContext.getSocketFactory());

		HttpURLConnection httpURLConnection = (HttpURLConnection) httpUrl.openConnection();

		httpURLConnection.setDoOutput(true);
		httpURLConnection.setRequestMethod("POST");
		httpURLConnection.setConnectTimeout(connectTimeoutMs);
		httpURLConnection.setReadTimeout(readTimeoutMs);
		httpURLConnection.connect();
		OutputStream outputStream = httpURLConnection.getOutputStream();
		outputStream.write(reqBody.getBytes(UTF8));

		//获取内容
		InputStream in = httpURLConnection.getInputStream();
		BufferedReader bufferedReader = new BufferedReader(new InputStreamReader(in, UTF8));
		final StringBuffer stringBuffer = new StringBuffer();
		String line = null;
		while ((line = bufferedReader.readLine()) != null) {
			stringBuffer.append(line);
		}
		String resp = stringBuffer.toString();
		if (stringBuffer!=null) {
			try {
				bufferedReader.close();
			} catch (IOException e) {
				// e.printStackTrace();
			}
		}
		if (inputStream!=null) {
			try {
				inputStream.close();
			} catch (IOException e) {
				// e.printStackTrace();
			}
		}
		if (outputStream!=null) {
			try {
				outputStream.close();
			} catch (IOException e) {
				// e.printStackTrace();
			}
		}
		if (certStream!=null) {
			try {
				certStream.close();
			} catch (IOException e) {
				// e.printStackTrace();
			}
		}
		return resp;
	}
	/**
	 * 需要证书的请求，默认30秒超时时间
	 * @param url String
	 * @param parameterMap post的请求数据  Map
	 * @param inputStream 证书输入流
	 * @param password	证书密码
	 * @return API返回数据
	 * @throws Exception
	 */
	public static String postWithCert(String url, Map<String, String> parameterMap,InputStream inputStream,String password) {
		try {
			return postWithCert(url, parameterMap, inputStream, password,30000,30000);
		} catch (Exception e) {
			e.printStackTrace();
			return null;
		}
	}

